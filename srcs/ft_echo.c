#include "shell.h"

int			ft_echo(t_env *e)
{
	int		i;

	i = 0;
	while (e->cmd[++i])
	{
		if (i > 1 && i < (int)e->cmd_len)
			ft_putchar(' ');
		ft_putstr(e->cmd[i]);
	}
	ft_putchar('\n');
	return (0);
}
